    <div style="margin-top:5em;" class="container25">
        <h2>Conclusion</h2>

        <p style="margin-top:1em;">This project was a very fruitful experience for us, as we had the chance, not only to develop
            our UX-UI Design skills but also our skills for Interior Design while exploring the interactions created by new relationships between
            spaces and interfaces. We even had the opportunity to create a mise en abyme of our own concept by asking feedback about it from a Scenography student,
            who advised us about the spatial arrangements of the exhibition hall.</p>

        <p style="margin-top:1em;">To finish, we believe that this service can truly promote new learning practices,
            which boost holistic and critical thinking, by sharing students' skills and knowledge. And this is core to a designer's job.</p>
