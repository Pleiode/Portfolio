    <div style="margin-top:5em;" class="container25">
        <h2>Description</h2>

        <p style=" margin-top:1em;">Agora is a learning ecosystem composed of a website and an app, as well as an
            exhibition space, allowing the students of the French design school<u style="text-decoration-color: #e41145"><a target="_blank" style="color:#e41145 ; letter-spacing:normal; font-weight:500;" href="https://en.lecolededesign.com/">L'École de design Nantes Atlantique (EDNA)</a></u>to receive
            and to give feedback from other students, in order to develop their critical thinking and improve their works.</p>


        <h2 style="margin-top:3em;">Context</h2>

        <p style=" margin-top:1em;">
            In partnership with the EDNA school, and studying at this school, we were asked to design new learning experiences
            for the school's students thanks to new digital tools, in line with the school's new building, and following the school's values.</p>

        <h2 style="margin-top:3em;">Motivation</h2>

        <p style="margin-top:1em;">
            We were very excited to reimagine our school's ways of working, which were deeply challenged by the Covid19 outbreak.
            Because we believe students can learn a lot from each other, our main objective was to develop the students' critical thinking
            and to encourage them to iterate their works further through peer editing.</p>

        <h2 style="margin-top:3em;">Brainstorming</h2>

        <p style="margin-top:1em;">
            To identify our objectives and potential directions for our design solution, we split the school's brief into 3 dimensions.
            First, the <mark>pedagogical program</mark> is central to convey humanist values to students and the search for critical thinking.
            Secondly, the school's new building is a powerful place to implement the program in the students' routine.
            And thirdly, a <mark>modular</mark> interface between students can enable them to develop their critical thinking by advising others.</p>

    </div>

