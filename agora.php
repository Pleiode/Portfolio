        <h3 style=" margin-top:2em;">Visual Identity</h3>

        <p style=" margin-top:1em;">Defining the digital interface, we conceived a visual identity that conveys a sense of``
            informal and benevolent sharing of knowledge, thanks to a lively colour contrast made of the blue of rationality, and a joyful red for passion.
            The name, Agora, refers to was a central public space in ancient Greek cities.</p>
    </div>

