        <h3 style="margin-top:2em; ">Exhibition area</h3>

        <p style=" margin-top:1em;">The school's future building is meant to have a very large and central hall,
            ideal for the exhibition of students' projects. It is indeed crucial for the users to have a physical interaction with
            the shared projects, to offer better feedback, and to feel more involved in this service. </p>
