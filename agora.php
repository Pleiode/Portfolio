        <p style="margin-top:1em;">Next, we designed modular exhibition furniture, which can be arranged in different ways: a
            pedestal and a shelf for exposing objects, mockups, books and magazines, a hanging stand to clip posters and photographs, a furniture
            for TV screens to play videos or show images, and a mobile phone stand to show apps and websites.</p>
