    <div style="margin-top:5em;" class="container25">
        <h2 style="margin-top:3em;">Survey</h2>

        <p style="margin-top:1em;">
            As our targeted users were the students of our own school, it was convenient to make a detailed survey, distributed to all the school's students.
            Among the 201 answers, here are some key observations we can make.</p> <br>

        <p style=" margin-top:1em;">
            - 92% of the students feel that the main obstacle they encounter while working on their school projects is a <mark>lack of feedback</mark> on their work
            after they submitted it. 53% think they lack feedback before the deadline (many possible answers were possible).</p> <br>

        <p style="margin-top:1em;">
            - 90% of the students believe that other <mark>students' feedback can help them</mark> in their learning experience.
            The students mainly wait for the following types of feedback from other students: design techniques and software,
            the accuracy of the direction taken in a design project, and inspiration and resources.</p>

        <h2 style="margin-top:3em;">Research</h2>

        <p style=" margin-top:1em;">
            Being aware of the students' frustration concerning the lack of feedback, and understanding their expectations
            for other students' advice, we proceeded to a phase of research, driven by 3 insights.</p> <br>

        <h3 style="margin-top:1em;">
            Insight 1: Alternative pedagogies could be explored by the EDNA school to develop critical thinking skills, which are essential for future designers.</h2>

            <p style=" margin-top:1em;">
                - The holistic theory considers the learner as a whole and <mark>values social relationships</mark> and <mark>humanist values</mark> in the learning environment.
                It also asks for addressing an issue from all its standpoints (social, economic, ecological...), which can be achieved through collaboration. <br>

            <p style=" margin-top:1em;">
                - These ideas are concretized by the Montessori pedagogy, which believes that the learner can <mark>discover its own knowledge</mark> thanks to individual
                and autonomous experimentation in modular environments, and by <mark>sharing its</mark> findings with others. <br>

            <p style=" margin-top:1em;">
                - The Freinet school also puts experimentation at the core of the learning process, as well as cooperation among peers, and formative assessment. <br> <br>



            <h3 style=" margin-top:1em;">
                Insight 2: The students can only develop mutual critical thinking if it is done in an ethical and constructive environment.</h2>

                <p style="margin-top:1em;">
                    To define how we can elaborate such an environment among the students, we interviewed the sociologist Elvire Bornand
                    about the complexities of a critical mind. Here are the key findings we collected:</p>

                <p style="margin-top:1em;">
                    - To adopt critical thinking, students first <mark>need to be curious, modest, to be lucid</mark> about themselves and about others
                    (know each ones' biases), and develop an <mark>intellectual autonomy</mark> by accepting that their own opinion is not the absolute truth.<br>

                <p style=" margin-top:1em;">
                    - But how to give useful feedback? Evaluating can be unproductive if it causes a <mark>sense of rivalry</mark> between students, or if it is biased by the assessor's
                    subjectivity or fear to discourage the assessed person. Therefore, an assessment must always <mark>evaluate a work</mark> and not a person.</p> <br> <br> <br>

                <h3 style=" margin-top:1em;">
                    Insight 3: The students' retention rate is higher when they have the chance to carry out actions on their own. This can be enabled through the adoption of new digital practices.</h2>

                    <p style="margin-top:1em;">
                        - Immersive technologies like AR, VR, and mixed reality, which can be used in a collaborative mode, can create fruitful learning experiences.
                        Tools like Béhance efficiently expose design projects, and instant messaging services like Messenger and Slack allow powerful interactions and feedback. </p><br>

                    <p style=" margin-top:1em;">
                        - We want to provide students with constructive feedback, and such feedback must be grounded in specific contexts of a design project.
                        Therefore, we found Google Docs' and Soundcloud's located comment features very accurate. </p><br>

