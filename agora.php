        <h2>Prototype</h2>

        <h3 style=" margin-top:2em; ">Wireframe</h3>

        <p style=" margin-top:1em;">The service is composed of a mobile and desktop app, as well as a web version.
            The two versions are the same, but both are essential. Indeed, students need to have access to the mobile
            app to give feedback instantly when they meet the projects in the exhibition space, as well as to explore
            the projects during transit in public transportation for example.</p>
    </div>

    <img loading="lazy" style="margin-top:2em; " src="https://jolypaul.com/res/agora/wireframe1.jpg" alt="wireframe1">

    <div style="margin-top:5em;" class="container25">
        <p style=" margin-top:1em;">The desktop version, on the other side, is more meant to be used to share projects on the platform.
            As the students have their project's files on the computer, it is easier to upload them from there.</p>
    </div>

