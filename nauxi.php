    <div style="margin-top:5em;" class="container25">
        <h2 >Description</h2>

        <p style="; margin-top:1em;">Nauxi is an application involving the general public not only in <mark> scientific research </mark>,
            but also in <mark> reforestation </mark> by collecting <mark> data about clouds </mark> to act in the face of climate change.</p>


        <h2 style="margin-top:3em;">Motivation</h2>

        <p style="; margin-top:1em;">
        Being very sensitive to the issue of global warming, I naturally wanted to tackle this subject during this
         project carried out as part of my Bachelor project at<u style="text-decoration-color: #e41145" ><a target="_blank" style="color:#e41145; ; letter-spacing:normal; font-weight:500; " href="https://en.lecolededesign.com/">L'École de design Nantes Atlantique.</a></u></p>

        <p style="; margin-top:1em;">
            Global warming is at the heart of today's issues, and what can better understand it and try to limit its impacts than science?
            Personally, I have always been drawn to science, biodiversity, and the protection of the environment. This is why I got interested in participatory science.</p>

        <h2 style="margin-top:3em;">Context</h2>

        <p style="; margin-top:1em;">
            <mark> - 50.6 billion </mark> euros is the amount of domestic expenditure on research and development (GERD) in 2017 in France. <br> <br>
            
            - This represents 2.2% of the French GDP.  <br> <br>
            Are French citizens up to date with current scientific research? 
            To have more information about this subject, I decided to carry out a survey.
        </p>

        <h2 style="margin-top:3em;">Survey</h2>

        <p style="; margin-top:1em;">
        This survey was carried out on 78 people (from 19 to 78 years) of various and varied professional activities. 
        We will call this sample of people, "the general public".</p>

    </div>

    <img loading="lazy" style="margin-top:2em;  " src="https://jolypaul.com/res/nauxi/survey1.png" alt="">
    <img loading="lazy" style="margin-top:2em;  " src="https://jolypaul.com/res/nauxi/survey2.png" alt="">
    <img loading="lazy" style="margin-top:2em;  " src="https://jolypaul.com/res/nauxi/survey3.png" alt="">

    <div style="margin-top:5em;" class="container25">
        <p style="; margin-top:1em;">
            Also, according to an IPSOS survey (May 2016), carried out on 1,009 people constituting a representative sample of the French population aged 15 and over. (with many possible responses).
            38% and 33% of these people would like to participate in research concerning <mark>biodiversity</mark> and <mark>climatology.</mark></p>

        <p style="; margin-top:1em;">
            We can conclude from this survey that the general public has a certain infatuation with scientific research.</p>


        <p style="; margin-top:1em;">
            By having defined the framework of my project, I was able to define my problematic.</p>




        <h2 style="margin-top:2em;">Problem</h2>

        <h2 style="; margin-top:1em;">
            <mark> How can the general public participate in scientific research? </mark>
        </h2>


        <h2 style="margin-top:2em;">Research</h2>

        <h3 style="; margin-top:2em; ;">Citizen science</h3>

        <p style="; margin-top:1em;">
            While doing research, I quickly converged on citizen science.</p> <br>

        <div class="callout">
            <p  style="; ">
            <i > " <mark> Citizen science </mark> can be defined as the participatory and combined effort of research, analysis and public
                education which strictly pursues, as a basic principle, the objective of collective well-being of present
                and future generations of human beings on the planet and the biosphere. "</i>
                <h4 style="color: black; margin-top:1em;">
            Mycle Schneider, ecologist and consultant for various ministries. Winner of the Right Livelihood Award2 in 1997.</h4>
            </p>
        </div>
        

        <p style="; margin-top:1em;">
            Participatory science makes it possible to directly <mark> create a "bridge" or an interaction between citizens and scientific researchers</mark> .
            So by accumulating data collected by a large public, scientists can use it to advance their research.</p>

        <h3 style="; margin-top:4em; ;">Cloud</h3>

        <p style="; margin-top:1em;">
            By taking an interest in one of the conferences of Kate Marvel, a renowned climatologist.
            I quickly understood that the <mark> study of clouds was essential to fully understand the climatological
                changes</mark> that have been occurring in recent years.  </p>

                <br> <div class="callout">
                <p style="; ">
                     <i> "Better understand clouds to better understand global warming." </i></p>
                     <br>
                     <h4 style="color: black;"> Kate Marvel - Climatologist - Columbia University and the NASA Goddard Institute of Space studies.</h4>
                </div>
            
        <br> <br>
        <p style=";">Current climate models hardly take clouds into account in their simulations. </p>
        <br>
        <div class="callout">
            <p style=";"><i> "Clouds double the temperature change: by keeping the targets and removing them from the equation,
                the rise at the end of the century would only be 1 ° C. This is one of our greatest uncertainties and the climate negotiations do not even mention it."</i> </p> <br>
            <h4 style="color: black;">Sandrine Bony - climatologist and Research Director at CNRS</h4>
            </p>
        </div>
        <h3 style="; margin-top:4em; ;">Deforestation</h3>

        <p style="; margin-top:1em;">
        As we have seen, biodiversity is of great interest in France. As we know biodiversity is in danger.
         And one of the main causes of this endangerment is deforestation.</p> <br>


        <div class="callout">
            <p style=";">
            <i> "Forest area increased from 31.9 to 31.2 percent of the world's
                land area between 2000 and 2020, <mark>a net loss of almost 100 million hectares of forests</mark> globally."</i> 
            </p> <br>
            <H4 style="color: black; ">
            Cites it FAO in a report [http://www.fao.org/sdg-progress-report/fr/ (2019)</h4></div>
        

    </div>


